				<p>(dalam bahasa Belanda: Buitenzorg) adalah sebuah kota yang terletak di provinsi Jawa Barat, Indonesia. Kota ini terletak 59 km di sebelah selatan Jakarta, dan merupakan enklave Kabupaten Bogor. Pada tahun 2021, jumlah penduduk kota Bogor sebanyak 1.075.457 jiwa, dengan kepadatan 9.075 jiwa/km2.</p>
